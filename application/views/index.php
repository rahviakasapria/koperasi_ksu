                                Dan dalam perkembangannya, Pra Koperasi Manggar Tuwuh terus melaju dan berperan dalam meningkatkan perekonomian masyarakat Desa Pageraji, 
                                sehingga kemudian terbentuklah Koperasi KSU "Manggar Tuwuh" yang berdiri pada tanggal 10 November 2005 dengan memperoleh 
                                ketetapan sebagai Koperasi yang berbadan hukum dengan nomor 141/BH/KDK. 11-15/XI/2005. Dan di era persaingan sekarang ini, 
                                KSU "Manggar Tuwuh" dituntut untuk tetap eksis. Salah satu cara yang dilaksanakan adalah meningkatkan pelayanan terhadap anggotanya dibidang simpan pinjam.
                            </p>

